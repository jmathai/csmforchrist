<h1>The town of Chikkaballapur</h1>
<p>
Chikkaballapur is the District Headquarters of the newly created Chikkaballapur district in the state of Karnataka, India.
</p>
<h2>The origin of its name</h2>
<p>
In the regional language, Kannada, the city is pronounced Chikkaballapura. 
"Chikka" in Kannada means "small", while "balla" means the measure to quantify food grains, and "pura" means "town". 
Thus, its a place where people used to use small measures to quantify the food grains in ancient times. 
The place has always been known as an agricultural center for the region.
</p>
