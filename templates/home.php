    <h2>Christianity in India</h2> 
    <p>
      It happens every day in America - someone decides to change relitiongs. 
      But in a country where it is taken for granted that when you are born into a religion, you will die in that religion, making a change from Hinduism to Christianity is a big deal.
    </p>
    <p>
      Although it's technicall legal to become a Christian, it is illegal to coerce someone to do so. 
      You can go to jail for up to two years for offering incentives to someon to change religions. 
      So in the early days of the ministry when Mathew received a knock at the door late at night he was quite startled to see two members of the Indian FBI.
      They showed him their badges and began to ask all kinds of questions.
      Then they simply left.
    </p>
    <blockquote><p>When you walk around the village you will see something different on the faces of the Christians. They have a joy they didn't have before. -- <em>Mathew</em></p></blockquote>
    <p>
      In the early days of the ministry, most new converts were ostracized by their families when they accepted Christ.
      Today it happens to only about one in four because Christhia Seva Mission is now highly regarded in the community.
    </p>
    <p>
      Christianity is no longer the thread it once was. 
      The community sees changes taking place in Christian's lives.
      They are friendly, they talk to neighbors.
    </p>
